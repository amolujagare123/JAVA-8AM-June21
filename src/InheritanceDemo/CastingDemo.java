package InheritanceDemo;

public class CastingDemo {

    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        double c = (double) a/b;

        // if you want to store one type of value in another type
        // then we do casting, here we use the type in round brackets
        // before the value to be stored in that particular varible

        System.out.println("c="+c);

    }
}
